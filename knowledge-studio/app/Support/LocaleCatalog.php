    /**
     * @return array{0:string,1:string}
     */
    private function splitUrl(string $url): array
    {
        $parts = explode('?', $url, 2);
        return [
            (string) ($parts[0] ?? ''),
            (string) ($parts[1] ?? ''),
        ];
    }

