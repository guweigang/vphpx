    public function resolve(?string $preferred, string $acceptLanguage = ''): string
    {
        $preferred = $this->normalize($preferred ?? '');
        if ($preferred !== '') {
            return $preferred;
        }

        $acceptLanguage = strtolower(trim($acceptLanguage));
        if ($acceptLanguage !== '' && str_starts_with($acceptLanguage, 'en')) {
            return 'en';
        }

        return 'zh-CN';
    }

    public function normalize(string $locale): string
    {
        $locale = strtolower(trim($locale));
        if ($locale === '') {
            return '';
        }
        if (str_starts_with($locale, 'en')) {
            return 'en';
        }

        return 'zh-CN';
    }

    public function withLang(string $url, string $locale): string
    {
        $locale = $this->resolve($locale);
        [$path, $query] = $this->splitUrl($url);
        parse_str($query, $params);
        if (!is_array($params)) {
            $params = [];
        }

        unset($params['lang']);
        if ($locale !== 'zh-CN') {
            $params['lang'] = $locale;
        }

        $query = http_build_query($params);
        return $query !== '' ? $path . '?' . $query : $path;
    }

